# This program demonstrates basic list operations: creating a list, checking its length, and appending an item.

def main():
    fruits_list = ["apple", "banana", "cherry" , "date", "elderberry"]

    print("Length of the list:", len(fruits_list)) #Length of the list

    fruits_list.append("mango") #Append an item
    
    print("Updated list:", fruits_list) #Updated list



if __name__ == "__main__":
    main()


